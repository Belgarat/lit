
<P CLASS="western" ALIGN=CENTER STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<FONT SIZE=5><B>Associazione Culturale </B></FONT>
</P>
<P CLASS="western" ALIGN=CENTER STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<FONT SIZE=5><I><B>LUX IN TENEBRA</B></I></FONT></P>
<P CLASS="western" STYLE="margin-bottom: 0cm"><BR>
</P>
<P CLASS="western" ALIGN=CENTER STYLE="margin-bottom: 0cm"><FONT SIZE=4><B>STATUTO</B></FONT></P>
<P CLASS="western" ALIGN=CENTER STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<B>Natura e scopi</B></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
1 - Viene costituita in Feltre l'Associazione Culturale <I>Lux in
Tenebra</I>, di seguito denominata <I>Lux in Tenebra.</I></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
2 - <I>Lux in Tenebra</I> si prefigge i seguenti scopi:</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- contribuire alla divulgazione e alla conoscenza della cultura del
gioco, inteso come passatempo intelligente, momento di socialit&agrave;
e divertimento, confronto sportivo e intellettuale, occasione di
contatto tra persone e culture diverse, percorso di miglioramento
personale.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- diffondere la documentazione e la corretta informazione sul gioco e
sul gioco di ruolo in particolare; 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- svolgere e promuovere attivit&agrave; culturali legate sia al mondo
ludico che a quello della narrativa, creando un legame tra il gioco e
la letteratura;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Per il conseguimento di tali scopi <I>Lux in Tenebra</I> si propone
di realizzare: 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- attivit&agrave; di gioco rivolte a singoli, associazioni o comunit&agrave;
di ogni genere, corsi didattici e ogni altra iniziativa culturale in
senso lato;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- incontri e scambi culturali con altre associazioni analoghe;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- progetti volti all&rsquo;avvicinamento dei giovani al mondo della
narrativa e della letteratura attraverso la mediazione del gioco;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- stage, rassegne, premi, spettacoli, mostre,
laboratori, tornei e corsi di formazione o perfezionamento;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- pubblicazioni a sostegno e corredo delle suddette iniziative
culturali.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
3 - <I>Lux in Tenebra</I> &egrave; un'associazione apolitica e senza
alcun fine di lucro, intendendo operare nel solo campo ludico,
culturale e didattico. Le eventuali entrate che l&rsquo;Associazione
ottenga nel corso delle sue attivit&agrave; verranno utilizzate per
coprire le spese. Similmente, eventuali eccedenze di bilancio
verranno reinvestite in programmi, pubblicazioni, partecipazione a
manifestazioni, tornei, esposizioni, organizzazione di manifestazioni
conformi allo scopo stesso dell'Associazione.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
4 - La sede legale dell'Associazione &egrave; stabilita in Feltre,
via Cart 40, 32032, e potr&agrave; essere spostata con delibera a
maggioranza semplice del Consiglio Direttivo. A seconda delle
esigenze il Consiglio Direttivo potr&agrave; istituire delle sedi
secondarie operative. L&rsquo;Associazione ha anche una sede
&ldquo;virtuale&rdquo; sulla rete Internet all&rsquo;indirizzo
<A HREF="http://www.luxintenebra.net/"><U><FONT COLOR="#0000ff">www.luxintenebra.net</FONT></U></A>.
L&rsquo;eventuale modifica di tale residenza virtuale non sar&agrave;
soggetta ad approvazione dei soci ma verr&agrave; gestita dal
Consiglio Direttivo in autonomia relativamente alle esigenze
contingenti che potrebbero verificarsi nel corso delle attivit&agrave;.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.25cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
5 - L'anno sociale coincider&agrave; con un anno solare (1 gennaio -
31 dicembre)</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
6 - Per lo svolgimento delle proprie iniziative <I>Lux in Tenebra</I>
si avvarr&agrave; dell'apporto di volontari e operatori esperti nel
settore ludico e narrativo, ai quali potr&agrave; essere corrisposta
una indennit&agrave; da stabilirsi di volta in volta sulla base
dell'impegno intellettuale e fisico che gli stessi avranno impiegato
per l'espletamento di ogni singola iniziativa. Esclusivamente nei
limiti necessari allo svolgimento di esse e nel caso ci&ograve; si
renda necessario per qualificare e specializzare le varie attivit&agrave;,
<I>Lux in Tenebra</I> potr&agrave; assumere lavoratori dipendenti o
avvalersi di prestazioni di lavoro autonomo.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
7- <I>Lux in Tenebra</I>, nel rispetto del
proprio oggetto sociale ed in ordine al conseguimento dei suoi scopi,
potr&agrave; farsi promotrice della costituzione, oppure potr&agrave;
aderire alle organizzazioni e/o federazioni tra associazioni analoghe
a livello nazionale ed internazionale, eventualmente anche adeguando
le proprie strutture ed il proprio Statuto qualora le norme di tali
organizzazioni lo richiedano.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<B>Dei Soci</B></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
8 - All'Associazione possono liberamente
aderire tutti i cittadini italiani e stranieri maggiorenni che si
riconoscano nel suo scopo e nel suo oggetto sociale e siano
interessati al perseguimento dei suoi scopi. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
I Soci si distinguono in Fondatori, Onorari, Ordinari e Giocatori.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
a) Fondatori: sono tali coloro che hanno
redatto e depositato il presente Statuto. Hanno gli stessi
diritti/doveri dei soci ordinari.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
b) Onorari: sono considerati Soci Onorari persone ed Enti scelti dal
Consiglio Direttivo tra coloro che abbiano o conseguano particolari
meriti nel campo ludico, culturale e didattico. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
c) Ordinari: Sono considerati Soci Ordinari tutti coloro che, previo
pagamento delle quote stabilite dal Consiglio Direttivo, vogliano far
parte dell'Associazione e svolgervi attivit&agrave;. Hanno
diritto di partecipare a tutte le iniziative dell&rsquo;Associazione,
alle assemblee ordinarie e straordinarie e hanno diritto di voto
attivo e passivo.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
d) Giocatori: sono i soci che si aggregano alla comunit&agrave; di
<I>Lux in Tenebra </I>attraverso l&rsquo;accesso da Internet,
compilando un modulo elettronico e senza pagare alcuna quota di
associazione, al solo scopo di partecipare alle attivit&agrave; di
gioco online. Essi possono partecipare alle
attivit&agrave; di <I>Lux in Tenebra</I>, comprese le assemblee, ma
non hanno diritto al voto, alle cariche sociali e ad altri incarichi
specifici. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Tra le categorie di Soci sopra elencate saranno considerati
&quot;sostenitori&quot; coloro che corrisponderanno una quota annua
doppia della quota associativa normale.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Le somme versate all'Associazione a qualunque
titolo non sono rimborsabili, ai sensi dell'art. 37 del Codice
Civile.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
9 - Chi intenda associarsi a <I>Lux in Tenebra</I> dovr&agrave;
compilare l&rsquo;apposito modulo (scaricabile anche dal sito
<A HREF="http://www.luxintenebra.net/"><U><FONT COLOR="#0000ff">www.luxintenebra.net</FONT></U></A>)
e comunicare i seguenti dati:</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
-Nome e Cognome;</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
-Luogo e data di nascita;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
-Professione;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
-Residenza e numero telefonico; 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
-Indirizzo email valido;</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
-Dichiarazione di presa visione dello Statuto
dell'Associazion</FONT>e (anch&rsquo;esso disponibile sul sito), e di
accettazione in ogni sua parte. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
I dati personali volontariamente forniti dagli utenti all'atto della
registrazione sul sito di <I>Lux in Tenebra</I> sono soggetti al
trattamento previsto dal D.L. 30 giugno 2003, n. 196, &quot;Codice in
materia di protezione dei dati personali&quot;, ed in particolare a
quanto prescritto dall'art. 7 (&quot;Diritto di accesso ai dati
personali ed altri diritti&quot;).</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Coloro che vogliano associarsi soltanto per giocare sul sito non
avranno altra regola da osservare. Tale modalit&agrave; associativa
consentir&agrave; di accedere ai forum di gioco e di scaricare dal
sito tutti i materiali necessari alla partecipazione al gioco.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Coloro che invece vogliano partecipare alle attivit&agrave; proposte
da <I>Lux in Tenebra</I> al di fuori dell&rsquo;ambiente virtuale
dovranno versare anche la quota associativa annuale con modalit&agrave;
stabilite dal Consiglio Direttivo.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Potranno essere richiesti ai Soci contributi straordinari in funzione
di attivit&agrave; non previste dai programmi ordinari. Ciascun
associato che desideri partecipare alle suddette attivit&agrave; non
potr&agrave; esimersi dal versare gli eventuali contributi
straordinari.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
10 - La qualifica di Socio si perde per dimissioni, morosit&agrave; o
per espulsione, con modalit&agrave; diverse a seconda dello status
dei soci (vedi art. 8):</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
a) Soci Ordinari: il socio Ordinario che voglia dimettersi prima
della scadenza dell'anno sociale dovr&agrave; comunicare per iscritto
la sua intenzione al Consiglio Direttivo: le dimissioni avranno
effetto immediato a partire dalla data della comunicazione. Non &egrave;
prevista la restituzione al Socio dimissionario della quota sociale
n&egrave; degli altri contributi eventualmente versati.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
I Soci Ordinari che non risultino in regola entro un mese dall'inizio
del nuovo anno sociale saranno considerati decaduti, perdendo
automaticamente il diritto a partecipare alle iniziative
dell'Associazione.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
b) Soci Giocatori: il socio Giocatore che non risulti attivo sul
Forum di gioco per pi&ugrave; di 2 mesi viene rimosso dal database in
seguito alla revisione annuale che si svolge ogni anno nel mese di
gennaio.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Tutti i Soci, qualunque sia il loro status, che svolgano attivit&agrave;
lesiva del buon nome o per la gestione di <I>Lux in Tenebra</I>
saranno richiamati via email dal Consiglio Direttivo che, qualora
tali comportamenti dovessero persistere, potr&agrave; dichiarare
decaduto il Socio senza alcun obbligo di restituzione delle quote
versate. Avverso la delibera di radiazione &egrave; ammesso il
ricorso scritto all'Assemblea ordinaria dei Soci entro 60 giorni
dalla data del provvedimento.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<B>Organi sociali</B></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
11 - Gli organi dell'Associazione sono:</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- il Consiglio Direttivo (presidente, segretario, tesoriere);</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
- l'Assemblea ordinaria dei Soci.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
12 - L'Assemblea ordinaria dei Soci si riunisce una volta l'anno per
l'approvazione del bilancio, per l'eventuale rinnovo delle cariche
sociali e per la verifica delle attivit&agrave; dell'Associazione. La
convocazione per l'Assemblea ordinaria verr&agrave; affissa nella
sede legale di <I>Lux in Tenebra</I> e annunciata sul sito tre
settimane prima della data della riunione.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
L'Assemblea ordinaria &egrave; valida in prima convocazione se &egrave;
presente almeno la met&agrave; dei Soci. In mancanza di tale numero,
si intende riconvocata entro 24 ore dalla prima convocazione e
ritenuta valida qualunque sia il numero dei Soci presenti. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
13 - L'Assemblea straordinaria potr&agrave; essere convocata dal
Presidente per deliberazione del Consiglio Direttivo o su richiesta
di almeno un quarto dei Soci aventi diritto di voto  per deliberare
sull'anticipato scioglimento dell'Associazione, le modifiche allo
Statuto e in tutti in quei casi in cui si renda necessario sentire il
parere dei Soci su fatti e attivit&agrave; straordinari. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
L'Assemblea straordinaria deve essere convocata da parte del
Presidente con lettera raccomandata ai Soci almeno 15 giorni prima
dell'adunanza, indicando in essa i motivi e l'ordine del giorno da
discutersi. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
I verbali delle delibere assembleari saranno
firmati dal Presidente dell'Associazione, dal segretario e dal
verbalizzatore o, in ogni caso, da quest'ultimo e da un altro membro
dell'Assemblea. Il verbalizzatore viene scelto tra i membri
dell'Assemblea dei Soci con diritto di voto e, in mancanza di
candidature, questo compito verr&agrave; assunto dal Segretario o dal
socio pi&ugrave; vecchio tra gli aventi diritto al voto. E' ammesso
farsi rappresentare per delega scritta. Ogni socio con diritto di
voto non pu&ograve; farsi carico di pi&ugrave; di due deleghe.</FONT></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
14 - Il<I> </I>Consiglio Direttivo &egrave; composto da Presidente,
Segretario, Tesoriere, eletti dall'Assemblea con mandato biennale.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Il Consiglio Direttivo definisce le linee organizzative e culturali
di <I>Lux in Tenebra</I>, prende contatti con Enti pubblici e privati
e rende esecutive le delibere adottate dall'Assemblea, accoglie ed
approva le domande di iscrizione di nuovi associati, convoca
l'Assemblea ordinaria e straordinaria, redige l'ordine del giorno e
provvede, a richiesta degli associati interessati, a fornire copia
degli atti ufficiali dell'Associazione. 
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
15 - Il Presidente ha la rappresentanza legale dell'Associazione, &egrave;
investito dei pi&ugrave; ampi poteri di ordinaria e straordinaria
amministrazione, presiede il Consiglio Direttivo, convoca
l'Assemblea, rappresenta <I>Lux in Tenebra</I> in tutte le occasioni
ufficiali. 
</P>
