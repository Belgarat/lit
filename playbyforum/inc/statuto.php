<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<B>Scioglimento</B></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
26 - L'Associazione ha durata illimitata; essa, tuttavia, potr&agrave;
essere anticipatamente sciolta, qualora essa non possa pi&ugrave;
perseguire lo scopo sociale o per tutti gli altri motivi previsti
dalla legge previa deliberazione dell'Assemblea straordinaria dei
Soci con ratifica di almeno un quinto di essi. I beni posseduti
dall&rsquo;Associazione verranno destinati secondo quanto deliberato
dall&rsquo;Assemblea.</P>

