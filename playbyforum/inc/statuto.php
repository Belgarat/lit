<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
25 - Al fine di garantire un elevato livello culturale e consulenze
competenti sulla linee culturali di <I>Lux in Tenebra</I>, &egrave;
data facolt&agrave; al Consiglio Direttivo di nominare un Comitato
Scientifico, comprendente anche persone esterne all'Associazione.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
Il Comitato Scientifico non potr&agrave; comunque in nessun modo
assumere impegni o obblighi a nome dell'Associazione, e avr&agrave;
sempre un ruolo consultivo.</P>
