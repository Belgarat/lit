23 - Lo Statuto &egrave; modificabile su decisione a maggioranza
semplice del Consiglio Direttivo e ratifica dell'Assemblea
Straordinaria dei Soci.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
24 - Le attivit&agrave; dell'Associazione possono anche essere
rivolte a Circoli, Comunit&agrave; Parrocchiali, Associazioni ed
Istituti impegnati in altri campi della cultura o nel sociale, nonch&egrave;
ai CRAL e alle Scuole Pubbliche e Private di ogni ordine e grado.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
