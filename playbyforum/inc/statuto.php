<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
18 - Le cariche di Presidente, Segretario e Tesoriere non sono
retribuite ma, in virt&ugrave; dei compiti che essi sono chiamati a
svolgere per l'Associazione, ai Soci che le ricoprono potranno essere
loro rimborsate, entro limiti preventivamente stabiliti dagli organi
statutari,<B> </B>le sole spese effettivamente sostenute a favore
dell'Associazione. 
</P>
