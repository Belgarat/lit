<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<B>Fondi e bilancio</B></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
19 - Il patrimonio di <I>Lux in Tenebra</I> &egrave; il fondo comune
costituito dalle quote da versarsi annualmente a cura di ciascun
associato, nonch&egrave; da eventuali contributi di privati, dello
Stato o di Enti e Istituzioni pubbliche finalizzati a sostegno di
specifiche e documentate attivit&agrave; o progetti, di organismi
internazionali, da donazioni e lasciti testamentari, da rimborsi
derivanti da convenzioni, da entrate derivanti da attivit&agrave;
commerciali e produttive marginali, da sponsorizzazioni.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
La quota annua potr&agrave; essere variata dal Consiglio Direttivo in
base al rendiconto annuale ed alle risorse economiche
dell'Associazione.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
