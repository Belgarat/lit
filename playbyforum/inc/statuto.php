21 - Il bilancio di <I>Lux in Tenebra</I>  viene presentato
all'Assemblea per l'approvazione, ed &egrave; sempre disponibile alla
visione di qualunque Socio ordinario che ne faccia richiesta.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%; page-break-after: avoid">
<B>Varie</B></P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="margin-left: 0.14cm; margin-right: 0.1cm; text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
22 - Il Consiglio Direttivo ha facolt&agrave; di redigere un
Regolamento Interno per la regolamentazione dei rapporti tra i Soci.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
