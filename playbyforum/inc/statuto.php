<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
16 - Il Segretario redige ed aggiorna i libri sociali, cura
l'esecuzione delle delibere del Consiglio Direttivo, sostituisce o
rappresenta il Presidente in caso di sua temporanea assenza o
impedimento e come il Presidente ha la rappresentanza legale
dell'associazione. Il Segretario ha la facolt&agrave; di scegliere un
vice segretario che lo rappresenti e lo affianchi nelle sue attivit&agrave;.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
17 - Il Tesoriere provvede alla gestione economica delle quote
versate e del patrimonio dell'Associazione e redige a fine anno il
rendiconto da sottoporre all'approvazione dell'Assemblea. Il
Tesoriere ha la facolt&agrave; di scegliere un vice tesoriere che lo
rappresenti e lo affianchi nelle sue attivit&agrave;.</P>
<P CLASS="western" ALIGN=JUSTIFY STYLE="text-indent: 0.39cm; margin-bottom: 0cm; line-height: 200%">
<BR>
